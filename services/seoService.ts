const OG_IMAGE  = `${BASE_URL}/og-image.png`;
const SITE_NAME = 'RhemaNotes';

// ── Types ─────────────────────────────────────────────────────────────────────

export interface PageMeta {
  title: string;
  description: string;
  canonical: string;
  ogImage?: string;
  ogType?: 'website' | 'article';
  /** ISO 8601 date string — used for article published time */
  publishedAt?: string;
  /** Keywords specific to this page */
  keywords?: string[];
  /** JSON-LD object to inject — serialised automatically */
  jsonLd?: object;
}


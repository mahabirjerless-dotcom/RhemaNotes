/**
 * seoService.ts
 *
 * Generates and injects dynamic <head> metadata for RhemaNotes.
 *
 * Used in two places:
 *   1. React app  — call `setPageMeta()` on route/sermon changes
 *   2. Cloudflare Worker — call `buildSermonMetaHTML()` to inject into
 *      the HTML response before serving (for crawlers that don't run JS)
 */

